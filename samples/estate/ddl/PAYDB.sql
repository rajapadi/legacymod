-- DB2 for z/OS DDL for the payroll audit schema.
CREATE TABLE PAYROLL_AUDIT (
    EMP_ID    CHAR(6)      NOT NULL,
    GROSS     DECIMAL(9,2),
    AUDIT_TS  TIMESTAMP,
    PRIMARY KEY (EMP_ID, AUDIT_TS)
);

CREATE TABLE PAY_ADJUSTMENT (
    ADJ_ID    INTEGER      NOT NULL,
    EMP_ID    CHAR(6)      NOT NULL,
    AUDIT_TS  TIMESTAMP    NOT NULL,
    ADJ_AMT   DECIMAL(9,2),
    ADJ_NOTE  VARCHAR(120),
    PRIMARY KEY (ADJ_ID),
    FOREIGN KEY (EMP_ID, AUDIT_TS)
        REFERENCES PAYROLL_AUDIT (EMP_ID, AUDIT_TS)
);

CREATE INDEX XPAYAUD1 ON PAYROLL_AUDIT (EMP_ID);

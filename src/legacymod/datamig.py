"""Stage 7b — data-migration planning.

Per approved unit, produces ``workspace/datamig/<unit>/``:

- ``target_ddl_postgres.sql`` — DB2 DDL converted to PostgreSQL with a
  documented type mapping; anything without a clean mapping is flagged
  ``-- REVIEW`` rather than silently converted.
- ``proposed_schema.sql`` — VSAM/copybook -> relational proposal from the
  record layouts: OCCURS -> child tables, REDEFINES flagged for human
  decision, COMP-3 -> NUMERIC(p,s), primary key from SELECT RECORD KEY.
  A reviewable draft, never silently final.
- ``convert_<record>.py`` — runnable flat-file converters (EBCDIC cp037
  -> UTF-8, COMP-3/binary unpacking driven by the layout) each with a
  ``--self-test`` round trip (pack -> unpack -> byte-identical).
- ``datamig.md`` — the mapping doc: verdict, DB2->PG type table,
  proposal rationale, column<->field name-similarity report (reported,
  not assumed), and how to run the converters.
"""

